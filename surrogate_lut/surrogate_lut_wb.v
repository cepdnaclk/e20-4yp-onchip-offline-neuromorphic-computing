    // -------------------------------------------------------------------------
    wire        cs      = wb_cyc_i & wb_stb_i &
                          (wb_adr_i[31:10] == BASE_ADDR[31:10]);
    wire [7:0]  lut_idx = wb_adr_i[9:2];   // word-aligned → strip lower 2 bits

    // -------------------------------------------------------------------------
    //  Wishbone Response — single clock cycle
    // -------------------------------------------------------------------------
    always @(posedge wb_clk_i) begin
        // Default: deassert ACK
        wb_ack_o <= 1'b0;
        wb_dat_o <= 32'h0000_0000;

        if (wb_rst_i) begin
            wb_ack_o <= 1'b0;
            wb_dat_o <= 32'h0000_0000;
        end
        else if (cs) begin
            if (!wb_we_i) begin
                // READ: return surrogate gradient in lower byte
                wb_dat_o <= {24'h000000, lut_rom[lut_idx]};
            end
            // WRITE: ignore dat_i, peripheral is read-only
            // ACK either way so master doesn't hang
            wb_ack_o <= 1'b1;
        end
    end

