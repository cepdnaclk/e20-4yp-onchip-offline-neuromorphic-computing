// =============================================================================
//  surrogate_lut_wb.v — Surrogate Gradient Lookup Table (Wishbone Slave)
//  Project: On-Chip Offline Neuromorphic Computing (FYP)
// =============================================================================
//
//  Description:
//      A read-only 256-entry ROM that maps 8-bit membrane potential values
//      to their pre-calculated fast sigmoid surrogate gradient equivalents.
//
//      Used during STATE 2 (Surrogate Substitution):
//          - CPU reads V_mem from data memory
//          - CPU queries this LUT using V_mem as address index
//          - CPU writes returned surrogate gradient back to same memory location
//          - STATE 3 (Learning) then reads surrogate values directly → no
//            runtime surrogate computation needed
//
//  Surrogate Function:
//      h(v) = 1 / (1 + k * |v / 128|)^2,  k=4.0
//      Output: 8-bit unsigned fixed-point, 0xFF = 1.0 (peak at v=0)
//
//  Address Scheme (Wishbone):
//      CPU uses V_mem (8-bit unsigned) as word-offset into this peripheral.
//      To query LUT for membrane potential v_mem:
//          wb_addr = BASE_ADDR + (v_mem_unsigned * 4)
//      Note: v_mem stored in memory is treated as raw 8-bit (unsigned index).
//            Signed interpretation is embedded in the pre-calculated values.
//
//  LUT Index → Signed V_mem mapping:
//      Index   0   = v_mem =    0  (peak, grad = 1.0)
//      Index   1   = v_mem =   +1
//      ...
//      Index 127   = v_mem =  +127
//      Index 128   = v_mem =  -128
//      ...
//      Index 255   = v_mem =   -1  (near-peak, grad ≈ 0.94)
//
//  Wishbone Interface: Classic (non-pipelined), single-cycle ACK
//  Bus Width: 32-bit (surrogate value returned in bits [7:0])
//  Writes: ignored but ACK returned (peripheral is read-only)
//
//  Parameters:
//      BASE_ADDR — base address of this peripheral in the SoC address map
//                  (1 KB aligned: lower 10 bits must be zero)
// =============================================================================

